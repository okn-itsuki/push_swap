#include "libft.h"

int	ft_isprime(int n)
{
	int	p;

	p = 5;
	if (n < 2)
		return (0);
	if (n == 2 || n == 3)
		return (1);
	if (n % 2 == 0 || n % 3 == 0)
		return (0);
	while (p * p <= n)
	{
		if ((n % p == 0) || (n % (p + 2) == 0))
			return (0);
		p += 6;
	}
	return (1);
}
